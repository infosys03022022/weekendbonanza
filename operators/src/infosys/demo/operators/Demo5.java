package infosys.demo.operators;

public class Demo5 {

	public static void main(String[] args) {
		int num1=10;
		System.out.println(num1<10);    //false
		System.out.println(num1>10);    // false
		System.out.println(num1==10);   // true
		System.out.println(num1>=10);    // true
		System.out.println(num1<=10);    // true
		System.out.println(num1!=10);    // false

	}

}
